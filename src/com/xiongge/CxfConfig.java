package com.xiongge;



public class CxfConfig {
	private String url;
	private String cxfInterface;
	private String rule;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getCxfInterface() {
		return cxfInterface;
	}

	public void setCxfInterface(String cxfInterface) {
		this.cxfInterface = cxfInterface;
	}

	public String getRule() {
		return rule;
	}

	public void setRule(String rule) {
		this.rule = rule;
	}


}
